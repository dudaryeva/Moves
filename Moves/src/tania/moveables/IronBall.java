package tania.moveables;

public class IronBall extends Ball {

	public void moveRight() {
		System.out.println("Iron ball can move 0.5 sm to the right");
	}

	public void moveLeft() {
		System.out.println("Iron ball can move 0.5 sm to the left");

	}

	public void moveUp() {
		System.out.println("Iron ball can move 0.5 sm up");

	}

	public void moveDown() {
		System.out.println("Iron ball can move 0.5 sm down");

	}

}
